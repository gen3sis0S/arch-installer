log_message () {
    local LEVEL="$1"
    shift
    local MESSAGE="$@"

    case "$LEVEL" in
        INFO)
            echo -e "\e[32m[INFO]\e[0m $MESSAGE"
            ;;
        WARN)
            echo -e "\e[33m[WARN]\e[0m $MESSAGE"
            ;;
        ERROR)
            echo -e "\e[31m[ERROR]\e[0m $MESSAGE"
            ;;
        *)
            echo -e "\e[34m[LOG]\e[0m $MESSAGE"
            ;;
    esac
}

